// Create an array that has three elements, try to use the 
// POP(), the PUSH(), the UNSHIFT() methods in array.

let cars = ["Ford", "Toyota", "Benz"]
cars.pop() //This is used to remove the last element from the array
cars.push("Volvo") //This is used to add a new element to the end of the array
cars.unshift("Honda") //This is used to add a new element at the start of the array

console.log(cars);